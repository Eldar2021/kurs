void main() {
  // List => [] тизме
  List birList = ['Kurs8', 12, true, 23.45, "Kurs8", 12, 232.45];
  print(birList[2]);

  // Set => {}
  Set birSet = {'Kurs8', 12, true, 23.45, 12, "Kurs8"};
  print(birSet);

  // Map => {} (dictionary) сөздүк
  Map birMap = {12: "On eki", "Hello": "Salam", 'Name': "At"};
  print(birMap['Hello']);
  print(birMap['Name2']);
}

/*
Key <------> Value
Ачкыч <------> Маани
Hello => Salam
name => at
*/
